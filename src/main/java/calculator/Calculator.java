package calculator;

public class Calculator {
    public int multiply(int a, int b, int c){
        return a*b*c;
    }

    public int add(int a, int b) {
        return a + b;
    }

    public int multiply(int x, int y) {
        return x * y;
    }

    public int sub(int a, int b){
        return a-b;
    }
}
